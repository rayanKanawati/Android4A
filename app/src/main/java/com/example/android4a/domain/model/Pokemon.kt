package com.example.android4a.domain.model

class Pokemon {
    private val name: String? = null
    private val height: String? = null
    private val weight: String? = null

    fun Pokemon() {}

    fun getName(): String? {
        return name
    }

    fun getHeight(): String? {
        return height
    }

    fun getWeight(): String? {
        return weight
    }
}